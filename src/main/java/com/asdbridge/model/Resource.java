package com.asdbridge.model;

public class Resource
{

	private String	name;
	private String	type;

	public Resource()
	{
		// Empty constructor
	}

	public String getName()
	{
		return name;
	}

	public String getType()
	{
		return type;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public void setType(String type)
	{
		this.type = type;
	}
}
